use std::io::Write;

/// Compute quantile threshold from the voxel grid, matching the browser's approach.
pub fn compute_threshold(grid: &VoxelGrid, pct: f32) -> f32 {
    let mut samples: Vec<f32> = grid.data.iter()
        .map(|v| (v[0] + v[1] + v[2]) / 3.0)
        .filter(|&l| l.is_finite() && l > 0.01)
        .collect();
    samples.sort_by(|a, b| a.partial_cmp(b).unwrap());
    if samples.is_empty() { return 0.1; }
    let idx = ((pct * samples.len() as f32) as usize).min(samples.len() - 1);
    samples[idx]
}

pub struct Mesh {
    pub vertices: Vec<[f32; 3]>,
    pub colors: Vec<[f32; 3]>,
    pub triangles: Vec<[u32; 3]>,
}

/// Run marching cubes on the voxel grid to extract an isosurface.
pub fn marching_cubes(grid: &VoxelGrid, threshold: f32) -> Mesh {
    let n = grid.n;
    let mut vertices: Vec<[f32; 3]> = Vec::new();
    let mut colors: Vec<[f32; 3]> = Vec::new();
    let mut triangles: Vec<[u32; 3]> = Vec::new();

    // Edge-to-vertex cache: maps (edge_key) -> vertex index
    // Edge key encodes the two voxel endpoints
    let mut edge_cache: std::collections::HashMap<u64, u32> = std::collections::HashMap::new();

    let density = |ix: usize, iy: usize, iz: usize| -> f32 {
        let v = grid.data[iz * n * n + iy * n + ix];
        (v[0] + v[1] + v[2]) / 3.0
    };

    let position = |i: usize| -> f32 {
        (i as f32 + 0.5) / n as f32 * 2.0 - 1.0
    };

    // For each cube in the grid
    for iz in 0..n-1 {
        for iy in 0..n-1 {
            for ix in 0..n-1 {
                // Get density at 8 corners
                let d = [
                    density(ix, iy, iz),
                    density(ix+1, iy, iz),
                    density(ix+1, iy+1, iz),
                    density(ix, iy+1, iz),
                    density(ix, iy, iz+1),
                    density(ix+1, iy, iz+1),
                    density(ix+1, iy+1, iz+1),
                    density(ix, iy+1, iz+1),
                ];

                // Compute cube index
                let mut cube_index = 0u8;
                for i in 0..8 {
                    if d[i] >= threshold { cube_index |= 1 << i; }
                }
                if cube_index == 0 || cube_index == 255 { continue; }

                let edges = EDGE_TABLE[cube_index as usize];
                if edges == 0 { continue; }

                // Corner positions
                let cx = [ix, ix+1, ix+1, ix, ix, ix+1, ix+1, ix];
                let cy = [iy, iy, iy+1, iy+1, iy, iy, iy+1, iy+1];
                let cz = [iz, iz, iz, iz, iz+1, iz+1, iz+1, iz+1];

                // 12 edges, each connecting two corners
                let edge_corners: [[usize; 2]; 12] = [
                    [0,1],[1,2],[2,3],[3,0],
                    [4,5],[5,6],[6,7],[7,4],
                    [0,4],[1,5],[2,6],[3,7],
                ];

                // Interpolate vertices on active edges
                let mut edge_verts = [u32::MAX; 12];
                for e in 0..12 {
                    if edges & (1 << e) == 0 { continue; }

                    let [c0, c1] = edge_corners[e];
                    let (i0, i1) = (
                        (cx[c0], cy[c0], cz[c0]),
                        (cx[c1], cy[c1], cz[c1]),
                    );

                    // Edge key: pack both voxel indices
                    let key = edge_key(i0, i1, n);
                    if let Some(&vi) = edge_cache.get(&key) {
                        edge_verts[e] = vi;
                        continue;
                    }

                    let t = if (d[c0] - d[c1]).abs() > 1e-10 {
                        (threshold - d[c0]) / (d[c1] - d[c0])
                    } else {
                        0.5
                    };
                    let t = t.clamp(0.0, 1.0);

                    let p0 = [position(i0.0), position(i0.1), position(i0.2)];
                    let p1 = [position(i1.0), position(i1.1), position(i1.2)];
                    let pos = [
                        p0[0] + t * (p1[0] - p0[0]),
                        p0[1] + t * (p1[1] - p0[1]),
                        p0[2] + t * (p1[2] - p0[2]),
                    ];

                    // Interpolate color from the grid
                    let uv = [pos[0]*0.5+0.5, pos[1]*0.5+0.5, pos[2]*0.5+0.5];
                    let rgb = grid.sample(uv[0], uv[1], uv[2]);
                    let lum = (rgb[0] + rgb[1] + rgb[2]) / 3.0;
                    let color = if lum > 1e-6 {
                        // Normalize by density, apply gamma
                        [
                            (rgb[0] / lum).min(2.0).powf(0.45),
                            (rgb[1] / lum).min(2.0).powf(0.45),
                            (rgb[2] / lum).min(2.0).powf(0.45),
                        ]
                    } else {
                        [0.5, 0.5, 0.5]
                    };

                    let vi = vertices.len() as u32;
                    vertices.push(pos);
                    colors.push(color);
                    edge_cache.insert(key, vi);
                    edge_verts[e] = vi;
                }

                // Generate triangles from the triangle table
                let tri_row = &TRI_TABLE[cube_index as usize];
                let mut i = 0;
                while i < tri_row.len() && tri_row[i] != -1 {
                    triangles.push([
                        edge_verts[tri_row[i] as usize],
                        edge_verts[tri_row[i+1] as usize],
                        edge_verts[tri_row[i+2] as usize],
                    ]);
                    i += 3;
                }
            }
        }
    }

    Mesh { vertices, colors, triangles }
}

fn edge_key(a: (usize, usize, usize), b: (usize, usize, usize), n: usize) -> u64 {
    let idx_a = a.2 * n * n + a.1 * n + a.0;
    let idx_b = b.2 * n * n + b.1 * n + b.0;
    let (lo, hi) = if idx_a < idx_b { (idx_a, idx_b) } else { (idx_b, idx_a) };
    (lo as u64) << 32 | hi as u64
}

/// Write mesh as binary little-endian PLY.
pub fn write_ply(mesh: &Mesh, path: &std::path::Path) -> std::io::Result<()> {
    let mut f = std::io::BufWriter::new(std::fs::File::create(path)?);

    // Header
    write!(f, "ply\nformat binary_little_endian 1.0\n")?;
    write!(f, "element vertex {}\n", mesh.vertices.len())?;
    write!(f, "property float x\nproperty float y\nproperty float z\n")?;
    write!(f, "property uchar red\nproperty uchar green\nproperty uchar blue\n")?;
    write!(f, "element face {}\n", mesh.triangles.len())?;
    write!(f, "property list uchar int vertex_indices\n")?;
    write!(f, "end_header\n")?;

    // Vertices
    for (pos, col) in mesh.vertices.iter().zip(mesh.colors.iter()) {
        f.write_all(&pos[0].to_le_bytes())?;
        f.write_all(&pos[1].to_le_bytes())?;
        f.write_all(&pos[2].to_le_bytes())?;
        let r = (col[0].clamp(0.0, 1.0) * 255.0) as u8;
        let g = (col[1].clamp(0.0, 1.0) * 255.0) as u8;
        let b = (col[2].clamp(0.0, 1.0) * 255.0) as u8;
        f.write_all(&[r, g, b])?;
    }

    // Faces
    for tri in &mesh.triangles {
        f.write_all(&[3u8])?;
        f.write_all(&(tri[0] as i32).to_le_bytes())?;
        f.write_all(&(tri[1] as i32).to_le_bytes())?;
        f.write_all(&(tri[2] as i32).to_le_bytes())?;
    }

    f.flush()?;
    Ok(())
}

